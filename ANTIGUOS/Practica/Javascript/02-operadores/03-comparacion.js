let a = 10;
console.log(a > 5);
console.log(a >= 5);
console.log(a < 5);
console.log(a <= 5);

//De igualdad
console.log(a == 5);
console.log(a != 5);
console.log(a == '10');

//Tipo de dato es con tres iguales
console.log(a === '10');

//Preguntar si son distintos tipos de dato o de valor
console.log(a !== '10');