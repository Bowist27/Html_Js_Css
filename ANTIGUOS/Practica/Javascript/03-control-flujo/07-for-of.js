// for

let animales = ['Chanchito','Dragon','Perrito'];

//Animal seria solo un supuesto
for (let animal of animales) {
    console.log(animal);
}

//Tambien hay una forma de hacerlo con un while
let i = 0;
while (i < animales.length) {
    console.log(animales[i]);
    i++;
}